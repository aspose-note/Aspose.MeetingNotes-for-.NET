namespace Aspose.MeetingNotes.Models
{
    /// <summary>
    /// Supported export formats for meeting content
    /// </summary>
    public enum ExportFormat
    {
        /// <summary>
        /// Microsoft OneNote format (.one)
        /// </summary>
        OneNote,

        /// <summary>
        /// Markdown format (.md)
        /// </summary>
        Markdown,

        /// <summary>
        /// Portable Document Format (.pdf)
        /// </summary>
        PDF,

        /// <summary>
        /// HyperText Markup Language (.html)
        /// </summary>
        HTML
    }
}
